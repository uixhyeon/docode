import { ref, onMounted, onUnmounted } from 'vue'

const sections = [
  { id: 'overview', title: 'ê°œìš”' },
  { id: 'features', title: 'ì£¼ìš” ê¸°ëŠ¥' },
  { id: 'userflow', title: 'ì‚¬ìš©ì í”Œë¡œìš°' },
